//This program will add any two numbers and return their sum
function add(a, b) {
  return a + b;
}

//This program will take 3 args and if any of three greater than 2 it will return true else false
function greaterThan2(a, b, c) {
  if (a > 2 && b > 2 && c > 2) {
    return true;
  } else {
    return false;
  }
}
